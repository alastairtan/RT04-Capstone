class Staff {
    firstName;
    lastName;
    leaveRemaining;
    nric;
    email;

    constructor(firstName, lastName, leaveRemaining, nric, email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.leaveRemaining = leaveRemaining;
        this.nric = nric;
        this.email = email;
    }
}

export default Staff;
