class Address {
    line1;
    line2;
    postalCode;
    buildingName;

    constructor(line1, line2, postalCode, buildingName) {
        this.line1 = line1;
        this.line2 = line2;
        this.postalCode = postalCode;
        this.buildingName = buildingName;
    }
}

export default Address;
